myTuple="Clear"
print(type(myTuple)) # str

myTuple1="Clear",
print(type(myTuple1)) # tuple


myTuple2=("Clear",)
print(type(myTuple2)) # tuple

myTuple3=("Clear")
print(type(myTuple3)) # str

myTuple4=("Clear","A","B","C")
print(type(myTuple4)) # tuple

myTuple5="Clear","A","B","C"
print(type(myTuple5)) # tuple